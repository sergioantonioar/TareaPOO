    private void botonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCalcularActionPerformed

        if (campoArista.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el valor de la arista");
        } else {
            String diametroFormato = String.format("%.2f", diametroCubo());
            campoDiametro.setText(diametroFormato);

            String areaFormato = String.format("%.2f", areaLateralCubo());
            campoArea.setText(areaFormato);

            String volumenFormato = String.format("%.2f", volumenCubo());
            campoVolumen.setText(volumenFormato);
        }
    }//GEN-LAST:event_botonCalcularActionPerformed

