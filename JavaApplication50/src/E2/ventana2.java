    private double diametroCubo() {
        //Se convierte en double el contenido del textfield
        return Double.parseDouble(campoArista.getText()) * Math.pow(3, 1.0 / 2.0);
    }

    private double areaLateralCubo() {
        return 4 * Math.pow(Double.parseDouble(campoArista.getText()), 2);
    }

    private double volumenCubo() {
        return Math.pow(Double.parseDouble(campoArista.getText()), 3);
    }

